                    	<li id="firstQuestion"><p class="question">What does Sales Crediting mean?
                        <span class="minusBgIcon" id="minusQ1" style="display:none;" onclick="hideAnswer(1);"></span><span class="plusBgIcon" id="plusQ1" style="display:block;" onclick="showAnswer(1);"></span></p>
                        <p class="answer" id="answer1" style="display:none;">Sales crediting is the process of determining who gets paid for a sale of products or services. For example, Paul and Jane both work on a deal and they both need to be paid commission for the sale. Jane put in most of the selling effort, as the account executive, and Paul was in a supporting role. Their manager determines that Jane should get 80% of the "credit" for the sale, and Paul will get 20%. 
<br/>

<br/>

NOTE: Crediting is a % calculation that is done before the compensation process takes place. 
<br/>

<br/>

In the above example, here is how it works.
<br/>

<br/>

Let’s say the total value of a deal is: $100,000
<br/>

<br/>

Total commissions to be paid out (determined by the company) is say, at a rate of 4% of total value of the deal: 4% x $100,000 = $4,000
<br/>

<br/>

Sales Credits for Jane: 80% 
Sales Credits for Paul: 20%
<br/>

<br/>

Commission for Jane: 80% x $4000 = $3,200
Commission for Paul:  20% x $4000 = $800
<br/>

<br/>


This is a very simple example. The real world is way more complicated.  As companies grow, they get more and more complex. Direct Sales, Channel Sales, Inside Sales, Overlays, Subscriptions, Services, Splits, Acquisitions, etc. complicate the calculations required to get to crediting, and compensation right. 
<br/>

<br/>

(LINEN helps solve these complex sales crediting problems.)
 
